package confirmation_trai;

public class Confirmation_trait {
String nom;
int prix;
int vola;
String saisiseur;
String img;






public String getImg() {
	return img;
}
public void setImg(String img) {
	this.img = img;
}
public String getSaisiseur() {
	return saisiseur;
}
public void setSaisiseur(String saisiseur) {
	this.saisiseur = saisiseur;
}
public int getVola() {
	return vola;
}
public void setVola(int vola) {
	this.vola = vola;
}
public String getNom() {
	return nom;
}
public void setNom(String nom) {
	this.nom = nom;
}
public int getPrix() {
	return prix;
}
public void setPrix(int prix) {
	this.prix = prix;
}
}
